package controlstatements;

public class operators {
public static void main(String[] args) {

	int a=15,b=10;
	System.out.println(a+b);
	System.out.println( a-b);
	System.out.println(a*b);
	System.out.println(a/b);
	System.out.println(a%b);
	
	float c=2.5f ,d=2.5f;
	System.out.println(c+d);
	System.out.println(c-d);
	System.out.println(c*d);
	System.out.println(c/d);
	System.out.println(c%d);
	
	short e=4500,f=1500;
	System.out.println(e+f);
	System.out.println(e-f);
	System.out.println(e*f);
	System.out.println(e/f);
	System.out.println(e%f);
	
	long g=48679L, h=15248L;
	System.out.println(g+h);
	System.out.println(g-h);
	System.out.println(g*h);
	System.out.println(g/h);
	System.out.println(g%h);
	
	
	
	
}
}